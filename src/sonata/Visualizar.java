    private void btVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVisualizarActionPerformed
        int linhaSelecionada = tabela.getSelectedRow();
        
        if(linhaSelecionada < 0){
            JOptionPane.showMessageDialog(this,"Selecione uma linha","Aviso",JOptionPane.WARNING_MESSAGE);           
        }else{
            this.telaCadastro.mostrarAluno(array.get(linhaSelecionada));
            this.setVisible(false);
        }
    }//GEN-LAST:event_btVisualizarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int selectedIndex = tabela.getSelectedRow();
        
        if(selectedIndex < 0){
            JOptionPane.showMessageDialog(this,"Selecione uma linha","Aviso",JOptionPane.WARNING_MESSAGE);
        }else{
            this.removeAluno(selectedIndex);
        }
    }//GEN-LAST:event_btExcluirActionPerformed

