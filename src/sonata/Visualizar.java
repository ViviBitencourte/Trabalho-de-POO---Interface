    private void removeAluno(int i) {
        array.remove(i);
        RemoveAlunoFromTable(i);
    }

    private void addAlunoToTable(Aluno a) {
        DefaultTableModel model = (DefaultTableModel)tabela.getModel();
        model.addRow(new Object[]{a.getNome(),a.getCpf(),});
    }     

    private void RemoveAlunoFromTable(int i) {
        DefaultTableModel model = (DefaultTableModel)tabela.getModel();
        model.removeRow(i);
    }
    
    public void RedesenhaTabela(){
        DefaultTableModel model = (DefaultTableModel)tabela.getModel();
        while(model.getRowCount() > 0){
            model.removeRow(0);
        }
        
        for (Aluno a : array) {
            model.addRow(new Object[]{a.getNome(),a.getCpf(),a.getCurso(),a.getTelefone(),a.getTelefone(),a.getNomeDaMae(),a.getNomeDoPai(),a.getEndereco()});
        }
    }
    
    
